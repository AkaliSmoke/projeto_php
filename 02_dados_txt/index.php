        <h2>Volkswagen</h2>

        <p>
            A história da Volkswagen (VW) começou na Alemanha em 1937,
            idealizada como o "carro do povo" (Volkswagen) sob o regime nazista
            para criar um veículo acessível. Projetado por Ferdinand Porsche,
            o icônico Fusca tornou-se a base da empresa. A marca se expandiu
            globalmente e chegou ao Brasil em 1953, consolidando-se como uma
            das maiores produtoras de automóveis do país.
        </p>
    </figure>

    <!-- Amarok -->
    <figure>
        <img src="img/Amarok.jpg" width="400" height="200" alt="Volkswagen Amarok">

        <figcaption>
            Lançada em 2010 como a primeira picape média da Volkswagen,
            a Amarok foi projetada para combinar robustez com a dirigibilidade
            de um carro de passeio. Produzida na Argentina, destacou-se pelo
            conforto e estabilidade, mas enfrentou críticas iniciais sobre a
            mecânica 2.0. O modelo foi revolucionado em 2018 com o motor V6,
            tornando-se referência em potência.
        </figcaption>
    </figure>

    <!-- Gol -->
    <figure>
        <img src="img/Gol.jpg" width="400" height="200" alt="Volkswagen Gol">

        <figcaption>
            O Volkswagen Gol, lançado em 15 de maio de 1980, foi líder de vendas
            no Brasil por 27 anos consecutivos (1987–2014). Projetado para substituir
            o Fusca, o hatch marcou época com motor a ar e depois a água,
            consolidando-se com diversas gerações (G1 a G7) e encerrando sua
            trajetória em 2022 após 42 anos de produção.
        </figcaption>
    </figure>

    <!-- Polo -->
    <figure>
        <img src="img/Polo.jpg" width="400" height="200" alt="Volkswagen Polo">

        <figcaption>
            Lançado na Europa em 1975 como uma versão econômica do Audi 50,
            o Volkswagen Polo consolidou-se como um hatch compacto premium,
            vendendo mais de 16 milhões de unidades em seis gerações.
            No Brasil, destacou-se desde 2002 pela tecnologia e hoje se posiciona
            como um hatch moderno baseado na plataforma MQB.
        </figcaption>
    </figure>

    <!-- T-Cross -->
