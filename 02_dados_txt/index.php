            Reconhecido pelo porta-malas espaçoso, tornou-se um grande
            sucesso de vendas e permaneceu em produção até 2023.
        </figcaption>
    </figure>

    <!-- Jetta -->
    <figure>
        <img src="img/Jetta.jpg" width="400" height="200" alt="Volkswagen Jetta">

